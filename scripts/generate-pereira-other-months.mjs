// Generates pereira-dez-2025.ts and pereira-nov-2025.ts by sampling a subset of
// the Jan/2026 dataset and remapping dates. Gives the period selector something
// meaningful to switch between without being identical.
//
// Run: node scripts/generate-pereira-other-months.mjs

import { writeFileSync, readFileSync } from "node:fs";

const janSource = readFileSync("./lib/data/pereira-jan-2026.ts", "utf8");
const eqIdx = janSource.indexOf("=", janSource.indexOf("pereiraJan2026"));
const startIdx = janSource.indexOf("[", eqIdx);
const endIdx = janSource.lastIndexOf("]");
if (startIdx < 0 || endIdx < 0) throw new Error("Could not parse pereira-jan-2026.ts");
const janRecords = JSON.parse(janSource.slice(startIdx, endIdx + 1));

function mulberry32(a) {
  return function () {
    a |= 0; a = (a + 0x6d2b79f5) | 0;
    let t = a;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function buildMonth({ ym, year, month, sampleSize, idPrefix, seed }) {
  // Weekdays of the target month.
  const daysInMonth = new Date(year, month, 0).getDate();
  const weekdays = [];
  for (let d = 1; d <= daysInMonth; d++) {
    const wd = new Date(year, month - 1, d).getDay();
    if (wd >= 1 && wd <= 5) weekdays.push(d);
  }
  const rnd = mulberry32(seed);
  // pick sampleSize records deterministically from jan dataset.
  const indices = [...Array(janRecords.length).keys()]
    .sort((a, b) => {
      const ra = mulberry32(seed + a)();
      const rb = mulberry32(seed + b)();
      return ra - rb;
    })
    .slice(0, sampleSize);

  let seq = 1;
  const records = indices.map((i) => {
    const src = janRecords[i];
    const day = weekdays[Math.floor(rnd() * weekdays.length)];
    return {
      ...src,
      id: `${idPrefix}-${String(seq++).padStart(3, "0")}`,
      dataAtendimento: `${ym}-${String(day).padStart(2, "0")}`,
    };
  });
  return records;
}

const dezRecords = buildMonth({
  ym: "2025-12", year: 2025, month: 12, sampleSize: 93,
  idPrefix: "PER-2025-12", seed: 20251215,
});
const novRecords = buildMonth({
  ym: "2025-11", year: 2025, month: 11, sampleSize: 87,
  idPrefix: "PER-2025-11", seed: 20251115,
});

function emit(filename, varname, records, periodoLabel) {
  const head = `// Auto-generated by scripts/generate-pereira-other-months.mjs
// Period: ${periodoLabel}. Subset of pereira-jan-2026 with remapped dates
// so the period selector produces a visibly different dashboard state.

import type { Atendimento } from "./types";

export const ${varname}: Atendimento[] = ${JSON.stringify(records, null, 2)};
`;
  writeFileSync(`./lib/data/${filename}`, head);
}

emit("pereira-dez-2025.ts", "pereiraDez2025", dezRecords, "Dezembro 2025");
emit("pereira-nov-2025.ts", "pereiraNov2025", novRecords, "Novembro 2025");

const dezHoras = Math.round(dezRecords.reduce((a, r) => a + r.horasPerdidas, 0) * 10) / 10;
const novHoras = Math.round(novRecords.reduce((a, r) => a + r.horasPerdidas, 0) * 10) / 10;
const dezDias = Math.round(dezRecords.reduce((a, r) => a + r.diasPerdidos, 0) * 10) / 10;
const novDias = Math.round(novRecords.reduce((a, r) => a + r.diasPerdidos, 0) * 10) / 10;
console.error(JSON.stringify({
  dez: { total: dezRecords.length, horas: dezHoras, dias: dezDias },
  nov: { total: novRecords.length, horas: novHoras, dias: novDias },
}, null, 2));
